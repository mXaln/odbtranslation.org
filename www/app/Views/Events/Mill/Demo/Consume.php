                <h4>Español - <?php echo __("mill") ?> - <span class="book_name">Biblical Foundations 1: Manuscript 3</span></h4>

                <div class="mill-heading">
                    <strong><sup>1</sup></strong>
                    <h2>KINDS</h2>
                </div>
                <div class="mill-heading">
                    <strong><sup>2</sup></strong>
                    <p>It’s good to admit that the Old Testament often seems distant to us as modern people. But this reality can also lead to confusion, and it can even discourage us from studying this part of the Bible. One helpful way to avoid becoming disheartened is to identify the kinds of things in the Old Testament that make us feel this way. When we know the kinds of distance that we often encounter, we’ll be better equipped to understand and to apply these Scriptures to our lives.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>3</sup></strong>
                    <p>There are many ways to catalog the main kinds of distance between us and the Old Testament, but for our purposes, we’ll just speak of three: first, theological distance; second, cultural distance; and third, personal distance. These three types of distance interconnect in many ways. But for the sake of discussion we’ll deal with them one by one, starting with the theological distance that confronts us as we study the Old Testament.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>4</sup></strong>
                    <h2>Theological</h2>
                </div>
                <div class="mill-heading">
                    <strong><sup>5</sup></strong>
                    <p>When we speak of theological distance, we’re thinking primarily about the differences between the revelation that Old Testament authors received and the fuller revelation that we’ve received in Christ. Every Christian realizes that the Old Testament presents theological viewpoints that don’t, at first, appear to correspond with the teachings of the New Testament. Consider just a few examples.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>6</sup></strong>
                    <p>In Genesis 22, God called Abraham to sacrifice his son, and God blessed him for his willingness to do it. But what would we think about someone today who believed that God had called him to sacrifice his son? We wouldn’t even begin to take such a theological claim seriously.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>7</sup></strong>
                    <p>In the days of Moses, God expected his faithful people to march as a great army from Egypt to the Promised Land. But we certainly would think it strange if we found a group of Christians literally marching through the wilderness of Sinai to reach the Promised Land.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>8</sup></strong>
                    <p>In the Old Testament, we read of men devoting themselves to God’s service by making Nazarite vows not to cut their hair. Or we learn that God ordained the temple in Jerusalem as the only place where his people were to worship. We read that God required his people to sacrifice animals as atonement for sins, and that he commanded the wholesale destruction of Canaanite cities, including women and children.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>9</sup></strong>
                    <p>Aren’t we all a bit puzzled that the Old Testament calls us to believe God revealed such things when they seem so different from what he has revealed in the New Testament? The list of these kinds of theological differences goes on and on. Whatever else we may say, there is certainly great theological distance between us and the Old Testament.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>10</sup></strong>
                    <blockquote>
                        <p>As God reveals himself to us, he does so in space and time… You think of the practice of the old covenant, where certain food laws were practiced and certain clean/unclean rituals. That was to be practiced by the Old Testament Israelites, yet, that variety is not necessary for us to practice today given the covenantal changes. One can do these kind of things, but it’s not necessary. It’s not covenantally significant. In fact, you’re to be very, very careful that when you demand people to do them because the Old Testament says to do these things, you could be falling into, say … “Where in order to have a right relationship with God we must come under the old covenant which now has been brought to fulfillment.” So, some of those variety of practices are not necessary because of covenantal changes. — Dr. Stephen J. Wellum</p>
                    </blockquote>
                </div>
                <div class="mill-heading">
                    <strong><sup>11</sup></strong>
                    <p>In addition to theological distance, another kind of distance that separates us from the Old Testament is the cultural distance between the ancient Near East and our modern world.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>12</sup></strong>
                    <h2>Cultural</h2>
                </div>
                <div class="mill-heading">
                    <strong><sup>13</sup></strong>
                    <p>Cultural distance between ourselves and the Old Testament exists because human cultures are constantly changing. Social structures shift. Older customs seem odd and outdated. Imagine visiting your own country just 200 years ago. Many of the differences would make us feel as if we’re in a different world, even though we are simply in a slightly different era. Now, if this is true of 200 years ago in the same place, how much more should we expect to find cultural differences between ourselves and what we read in the Old Testament? So many differences exist between the ancient Near East and our modern world that many things we read in the Old Testament are strikingly unfamiliar.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>14</sup></strong>
                    <p>We feel cultural distance when the Old Testament describes life in various places in the ancient world, whether in Israel, Canaan, Egypt, Assyria, Babylon, Persia or the many other cultures mentioned in the Old Testament. The characters we encounter had countless cultural beliefs, values and practices, just like we do today. But they were often very different from our modern cultural beliefs, values and practices. Consider just a few examples of human culture that appear in the Old Testament:</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>15</sup></strong>
                    <p>Much of the Old Testament describes an agrarian world. We read about farming and animal husbandry throughout the pages of Scripture, but many modern urban people can barely imagine what went on in this ancient lifestyle. Technologies were also very different in the days of the Old Testament. The technologies that many of us rely on today to communicate, work and function simply did not exist in ancient times.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>16</sup></strong>
                    <p>In the Old Testament, we read about arranged marriages. We also learn that important biblical figures practiced polygamy. Various forms of slavery appear in the Old Testament. And both within Israel and outside of Israel, cultures were deeply impacted by the political realities of imperialism. There were exceptions to the rule, of course, especially among desert tribes, but great kings or emperors and their powerful armies play a crucial role throughout these Scriptures. The characters of the Old Testament knew practically nothing about modern democratic ideals.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>17</sup></strong>
                    <p>When we see these and similar features of Old Testament life, we’re often left wondering how to handle them. What are we to do with a Bible that is so deeply embedded in cultures that were so different from our own? Cultural differences cause us to sense an enormous gap between ourselves and the Old Testament.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>18</sup></strong>
                    <blockquote>
                        <p>I think if you look back, for example, to the Old Testament, you’ll find that there are things there that are so different from our setting today. For one thing, the Old Testament was quite agricultural in its setting and practices… Secondly, for example, the practice of polygamy was quite common… And so that’s culturally distant from us. — Dr. Luis Orteza</p>
                    </blockquote>
                </div>
                <div class="mill-heading">
                    <strong><sup>19</sup></strong>
                    <p>Having considered how theology and culture in the Old Testament often seem strange to us, we should look at a third kind of distance — what we may call “personal” distance.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>20</sup></strong>
                    <h2>Personal</h2>
                </div>
                <div class="mill-heading">
                    <strong><sup>21</sup></strong>
                    <p>When we speak of “personal” distance, we refer to the fact that people who lived in the days of the Old Testament were different from modern people. Many of the gaps between us and them often involve very personal, human factors. Of course, the people of the Old Testament were not entirely different from us. As we’ll see later in this lesson, they were similar to us in a number of important ways. But at the same time, they had strikingly different personal experiences that made them quite different from who we are today.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>22</sup></strong>
                    <p>We shouldn’t be surprised that people in Old Testament times were different from people in our day. After all, many of their dispositions and activities grew out of the theological and cultural world in which they lived. Think about it this way: On a theological level, many individuals in the Old Testament had remarkable spiritual experiences unlike any that we have today. They had visions of heaven and heard the audible voice of God. They interacted with heavenly beings. Stop for a moment and ask yourself, how would you be different if you had these kinds of spiritual experiences? What kind of person would you be if you had experienced divinely inspired visions, auditions, angels and demons and the like? Without doubt, such experiences would change us dramatically. Realizing this truth helps us recognize that we are different from the people of the Old Testament in many significant ways.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>23</sup></strong>
                    <p>More than this, consider how we are different from Old Testament people because of cultural influences. In the Old Testament, people often filled cultural roles that are foreign to us. They were kings, queens, servants and slaves. Men and women viewed their roles very differently than we view our roles today. The personalities of Old Testament people were deeply impacted by the horrors of ancient warfare. Going through famines and droughts without the aid of modern technology affected the physical and emotional health of ancient peoples. Many of us today have never faced these kinds of situations. And as a result, we often have a hard time identifying with people described in the Old Testament.</p>
                </div>
                <div class="mill-heading">
                    <strong><sup>24</sup></strong>
                    <p>Now that we’ve acknowledged the causes and kinds of distance that separate the Old Testament from us, we should turn to our second topic: the relevance of the Old Testament for modern people. If the Old Testament was written so long ago, why should we have any expectation that it’s still relevant for us today?</p>
                </div>
