    <h3 class="center">Content Philosophy</h3>
    <h4 class="center"><i>Values</i></h4>

    <p><strong>Our Mission</strong> ~ To make the life-&shy;‐changing wisdom of the Bible understandable and accessible
        to all.</p>
    <p><strong>Our Vision</strong> ~ To see people of all nations experiencing a personal relationship with Christ,
        growing to be more like Him, and serving in a local body of His family. </p>
    <p><strong>Our Content Values</strong> ~ With the Bible as our foundation, under the supremacy of Christ,
        we seek to be careful in our interpretation and application of Scripture, appropriate in our tone, and clear
        in our communication of the story of the Bible.</p>
    <p><strong>Our Brand</strong> ~ Encouraging is our brand essence. We seek to demonstrate that by producing material
        that is relational, relevant and trustworthy.</p>
    <p><strong>Our Promise</strong> ~ To help readers draw closer to God every day</p>

    <h3 class="center">Our Content Values Defined</h3>
    <p class="center"><strong>The Bible as Our Foundation</strong></p>

    <p>Our teaching needs to rest in the self-&shy;‐revelation, story, and wisdom of God.</p>

    <div style="padding-left: 50px">
        <p><i>Hope and encouragement stems from helping others discover what the God of the Bible
                tells us about Himself and what He has done for us in Christ.</i></p>

        <p><i>We want to help others see that the Bible is the great God-&shy;‐given story of our Creation,
                Rebellion, Rescue, and Restoration.</i></p>

        <p><i>In our teaching we attach authority to no more and no less than what the Scriptures say.</i></p>

        <p><i>We recognize the importance of helping others make connections between the story of Scripture and their
                own story and ongoing spiritual growth.</i></p>
    </div>

    <p class="center"><strong>The Centrality and Supremacy of Christ</strong></p>

    <p>Our teaching should point ultimately and consistently to Christ as the living and eternal Word of God.</p>

    <div style="padding-left: 50px">
        <p><i>We desire to communicate the love of our Father in heaven who has revealed His heart in the life, death,
                and resurrection of His Son.</i></p>

        <p><i>The Bible leads us to see Jesus as the dominant theme of the Old and New Testaments. We want to present
                Him as our eternal God, Creator, Sustainer, Teacher, Savior, Judge, and Lord.</i></p>

        <p><i>We need to point people to the gift of forgiveness God has offered to everyone through the cross and
                resurrection of Jesus as the full payment for our sin.</i></p>
    </div>

    <p class="center"><strong>Carefulness of Interpretation and Application</strong></p>

    <p>Our teaching should reflect the intent of Scripture, the wisdom of God, and the brokenness of life that links
        us to the original authors and hearers of Scripture.</p>

    <div style="padding-left: 50px">
        <p><i>We want to present the characters of the Bible as real people with strengths as well as faults, who lived
                in a real world.</i></p>

        <p><i>It is important to help others understand individual parts of the Bible in both their<br />own context and
                in light of the bigger story of God&rsquo;s patience, love, and mercy toward those who have walked
                away from Him.</i></p>

        <p><i>We need to present the Bible as the story of God&rsquo;s grace and wisdom in calling all generations back
                to Himself.</i></p>

        <p><i>We need to help others discover what it means to live in the forgiveness, love, name, merits, Spirit,
                and mission of Christ.</i></p>
    </div>

    <p class="center"><strong>Appropriate Tone and Clarity of Communication</strong></p>

    <p>Our teaching should reflect the attitudes of the Spirit of Christ and the ministry brand, and must not be obscured
        by insider terms and jargon that are not clearly explained in their context.</p>

    <div style="padding-left: 50px">
        <p><i>Our message needs to be marked by urgency and patience; truth and grace; conviction without condemnation;
                and care without compromise.</i></p>

        <p><i>Our content needs to be written and presented with humility, showing respect for those who disagree with us
                while distinguishing between ideas and people.</i></p>

        <p><i>Because of the growing number of people who are not familiar with either the Bible or the language of faith,
                we have a responsibility to express our message with freshness and creativity.</i></p>

        <p><i>We recognize the need for consistency in tone and voice throughout all our content so that everything is
                recognizable in voice as being encouraging, relational, relevant and trustworthy.</i></p>

        <p><i>We recognize that while teaching is our primary intention, not all content will be &ldquo;educational&rdquo;
                in nature. Some will aim to inspire more than instruct.</i></p>
    </div>
